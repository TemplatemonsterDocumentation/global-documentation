	<h3>Widgets import</h3>

	<h4>In order to import widgets to your website you need to install "Widget Importer & Exporter" plugin</h4>

	<p>Navigate to <strong>"Tools" = > "Widget Importer & Exporter"</strong> section after the successful plugin installation and activation. To start import select the file in .wie format. It is located in "Widget Dummy Content" folder in template package.</p>

	<p>When widgets are successfully imported you will see a list of widgets with installation status. Then you'll be able to start using them at site.</p>

