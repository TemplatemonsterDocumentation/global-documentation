<h4>Installing Facebook Widget plugin</h4>

<ol class="index-list">
	<li><p> Go to Plugins > Add New</p></li>
	<li><p> Type in the name of the WordPress Plugin or descriptive keyword, author, or tag in Search Plugins box or click a tag link below the screen </p></li>
	<li><p> Find the WordPress Plugin you wish to install </p></li>
	<li><p> Click Details for more information about the Plugin and instructions you may wish to print or save to help setup the Plugin</p></li>
	<li><p> Click Install Now to install the WordPress Plugin </p></li>	
	<li><p> The resulting installation screen will list the installation as successful or note any problems during the install </p></li>
	<li><p> If successful, click Activate Plugin to activate it, or Return to Plugin Installer for further actions.  </p></li>
</ol>
