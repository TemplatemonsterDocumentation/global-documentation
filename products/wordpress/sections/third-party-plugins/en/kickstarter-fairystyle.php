<h3>Track Kickstarter Project</h3>

<p>This professional <a href="https://wordpress.org/plugins/track-kickstarter-project/" target="_blank">Track Kickstarter Project plugin</a> allows to track all of your Kickstarter projects and show up stats on your blog. </p>

<p>Use  [tf_track_project] shortcode to add the widget to the site on the fly. </p>
	
<figure class="img-pinterest">
	<img src="img/third_party_plugins/track-kickstarter.png" alt="">
</figure>
<h4>Installing Track Kickstarter Project</h4>
<ol class="index-list">
	<li><p> Unzip the downloaded zip file.</p></li>
	<li><p> Upload the plugin folder into the wp-content/plugins/ directory of your WordPress site.</p></li>
	<li><p> Activate Track Kickstarter Project from Plugins page</p></li>
</ol>

<p>
	To customize the widget, navigate to the "Widgets" section of our documentation  and find <a href="#" target="_blank">Track Kickstarter Project</a>.
</p>
