<h3>TM Youtube Subscribe Widget Plugin</h3>

<p>This custom designed plugin from TemplateMonster allows you add YouTube subscribe widget to the site. In this way, users will be able to navigate to your YouTube channel right from the homepage.  </p>
	
<figure class="img-pinterest">
	<img src="img/third_party_plugins/tm-youtube.png" alt="">
</figure>
<h4>TM Youtube Subscribe Widget plugin</h4>

<ol class="index-list">
	<li><p> Upload "TM Youtube Subscribe Widget" folder to the "/wp-content/plugins/" directory</p></li>
	<li><p> Activate the plugin through the "Plugins" menu in WordPress</p></li>
	<li><p> Navigate to the "Widgets" section to start customizing</p></li>
</ol>
<p>
	To customize the widget, navigate to the "Widgets" section of our documentation  and find <a href="http://documentation.templatemonster.com/index.php?project=stylepark&lang=en&section=widgets#tm-youtube-widget" target="_blank">TM Youtube Subscribe Widget</a>.
</p>
