<h4>Installing Pinterest plugin</h4>

<ol class="index-list">
	<li><p> First install our plugin. Make sure you have activated it</p></li>
	<li><p> Pinterest Widget Plus is a widget so you have to get in to widget area and Drag and Drop it on your widget positions.</p></li>
	<li><p> Then click "Add new" then type the Pinterest Widgets in the search field</p></li>
	<li><p> Install and activate our Pinterest Widgets plugin</p></li>
	<li><p> To make the widget work perfectly you need to know some informations:</p></li>
	<ul class="marked-list">
	<li>
		Title: Title of your widget. It can be anything you wish
	</li>
	<li>
		Pinterest Username: Pinterest Username - If your pinterest profile url is- <a href="http://www.pinterest.com/BritneySpears" target="_blank"></a> then your pinterest username will be : BritneySpears so pattern is : <a href="http://www.pinterests.com/YOUR_PINTEREST_USERNAME" target="_blank"></a>
	</li>
	<li>	
		Image Width: Image scale width of Pinterest Pins images showing on site. Height will scaled auto
	</li>
	<li>	
		Board Width: Board Width of Pinterest in px
	</li>
	<li>
		Board Height: Board Height of Pinterest in px
	</li>	
	<li>
		Show Pin Widget: You can switch it to No if want
	</li>
	<li>	
		Pinterest Pin ID: This settings to display a single pinterest pin. Example: <a href=" http://www.pinterest.com/pin/99360735500167749/">http://www.pinterest.com/pin/99360735500167749/</a> this is your pinterest pin URL. So the "Pinterest Pin ID" is - the numeric numbers on the URL. Pinterest Pin ID: 99360735500167749 (This is an example, specify your ID code when adjusting the plugin)
	</li>
	<li>
		Show Follow Button: Yes or No to switch the button on and off
	</li>
	<li>	
		Pinterest User URL: Its same like top
	</li>
	<li>	
		Pinterest Full Name: Pinterest User Full Name. Example - Britney Spears
	</li>
	<li>	
		Show Pin It Button: You can switch it to YES/NO to on or off pin it button
	</li>
	<li>	
		External URL for pin : URL of your wish
	</li>
	<li>
		Image for Pin It Button : Image want to display
	</li>
	<li>	
		Description for Pin It Button : Description of your wish
	</li>
</ul>		
</ol>
