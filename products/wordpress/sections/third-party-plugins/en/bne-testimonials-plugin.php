<h4>Installing BNE Testimonials plugin</h4>

<p>So, let's take a closer look at how to install this plugin</p>
<ol class="index-list">
	<li><p> Upload "bne-testimonials" folder to the "/wp-content/plugins/" directory</p></li>
	<li><p> Activate the plugin through the "Plugins" menu in WordPress</p></li>
	<li><p> A new menu item "Testimonials" will be added</p></li>
	<li><p> Add either "[bne_testimonials_list]" or "[bne_testimonials_slider]" to a post/page or use the available widgets in a sidebar.</p></li>
</ol>
<p>
	To customize the widget, navigate to the "Widgets" section of our documentation  and find <a href="#" target="_blank">BNE Testimonial Slider</a>.
</p>
