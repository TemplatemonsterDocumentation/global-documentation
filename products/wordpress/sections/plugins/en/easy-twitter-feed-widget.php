<h3>Easy Twitter Feed Widget Plugin</h3>

<p>
	Easy Twitter Feed Widget plugin uses the Twitter Widget without creating an API to display tweets on your WordPress site. There is no need to create Twitter application. Easy Twitter Feed Widget Plugin provides a nice interface to implement your tweets in an easy way.

	If you are planning to display tweets on your blog's sidebar without bells and whistles than our plugin can be your ideal choice.
</p>

	<figure class="img-polaroid">
		<img src="projects/<?php echo $project;?>/img/plugins/easy-twitter-feed-widget.png">
	</figure>

<p>
	Easy Twitter Feed Widget Plugin Options: You can control the loading of Twitter script manually. You may disable it, if your WordPress theme supports twitter script already.
</p>

<p>
	Easy Twitter Feed Widget: You may place your twitter feed in the sidebar or any other widgetized area of your WordPress theme by placing Easy Twitter Feed Widget.
</p>


