<h3>Track Kickstarter Project</h3>

<p>
	Easly track your Kickstarter projects and show up stats in your blog with Track Kickstarter Project widget or [tf_track_project] shortcode.
</p>

<h5>Configuration</h5>

<ul>
	<li>
		Shortcode. Add into editor next shortcode: [tf_track_project url="{your project page URL from kickstarter}" descr="{Your project description}"]
	</li>
	<li>
		Widget. Use Track Kickstarter Project at widgets page. Options the same as shortcode.
	</li>
	<li>
		You can rewrite in your theme widget and shortode template - just copy it from the templates folder in plugin and put into track-kickstarter folder in your theme
	</li>
</ul>

<p>Data getting from Kickstarter caching for 4 hours to prevent multiple requests to Kickstarter.</p>

